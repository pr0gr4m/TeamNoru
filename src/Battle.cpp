void Battle::BattleSystem() {
	while (1) {
	BAT_START:
		int battleget = 0;
		clrscr();
		ClearReadBuff();
		battleget = BattleMenu();
		while (1) {
			bool isCheck = false;
			int damage = 0;
			switch (battleget) {
			case 1:
				damage = p.attackDamage();
				_gotoxy();
				cout << "이얍! 평타!";
				getKey();
				monsterbeaten();
				_gotoxy();
				cout << p.name << "은(는) " << monster.beatenDamage(damage) << " 데미지를 " << monster.name << "에게 주었다!";
				getKey();
				isCheck = true;
				break;
			case 2:
				isCheck = ShowInventory();
				break;
			case 3:
				isCheck = ShowSkills();
				break;
			case 4:
				if (Run())
					return;
				isCheck = true;
				break;
			default:
				break;
			}
			if (isCheck)
				break;
			else
				goto BAT_START;
		}
		if (monster.isDie()) {
			_gotoxy();
			cout << p.name << "은(는) " << monster.name << "의 전투에서 승리했다!";
			getKey();
			p.Getexp(monster.Exp());
			_gotoxy();
			cout << p.name << "은(는) " << monster.Exp() << "의 경험치를 얻었다!";
			getKey();
			return;
		}
		AIBattle();
		if (p.nHp <= 0) {
			_gotoxy();
			cout << p.name << "은(는) 패배했다.....";
			getKey();
			GAMEOVER();
		}
	}
}


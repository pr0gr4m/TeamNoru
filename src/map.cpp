void mapDraw(int mapNum)
{
	int i;
	int j;

	setConsoleSize();

	for (i = 0; i < y_size; i++)
	{
		cout << "  ";
		for (j = 0; j < x_size; j++)
		{
			switch (map[mapNum][i][j])
			{
			case 0:
				cout << "  ";
				break;
			case 1:
				cout << "■";	// 벽
				break;
			case 2:
				cout << "＃";	// 입구
				break;
			case 3:
				cout << "＃";	// 출구
				break;
			case 4:
				cout << "＆";	// NPC
				break;
			case 5:
				cout << "※";	// 아이템
				break;
			case 6:
				cout << "※";	// 기믹
				break;
			case 7:
				cout << "♨";	// 체력회복
				break;
			case 8:
				cout << "●";	// 보스
				break;
			case 9:
				cout << "Ｐ";	// 플레이어
				break;
			case 10:
				cout << "■";	// 비밀문
				break;
			default:
				cout << "  ";
				break;
			}
		}
		cout << endl;
	}
}


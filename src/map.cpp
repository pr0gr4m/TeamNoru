void mapDraw(int mapNum)
{
	int i;
	int j;

	setConsoleSize();

	for (i = 0; i < y_size; i++)
	{
		cout << "  ";
		for (j = 0; j < x_size; j++)
		{
			switch (map[mapNum][i][j])
			{
			case 0:
				cout << "  ";
				break;
			case 1:
				cout << "¡á";// º®
				break;
			case 2:
				cout << "££";// ÀÌµ¿
				break;
				/*	case 3:
				cout << "£¦";// npc
				break;
				case 4:
				cout << "¡Ø"; //
				break;*/
			default:
				cout << "  ";
				break;
			}
		}
		cout << endl;
	}

}

